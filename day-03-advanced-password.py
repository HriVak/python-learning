print("=== ПРОВЕРКА НА СИЛНА ПАРОЛА ===\n")

password = input("Въведи парола: ")

# Проверки
дължина_ок = len(password) >= 8
има_главна = any(c.isupper() for c in password)
има_малка  = any(c.islower() for c in password)
има_цифра  = any(c.isdigit() for c in password)
има_специален = any(c in "!@#$%^&*()_+-=[]{}|;:,.<>?" for c in password)

print("Резултат от проверката:\n")

if дължина_ок and има_главна and има_малка and има_цифра and има_специален:
    print("ОТЛИЧНА ПАРОЛА! Много силна и сигурна!")

elif дължина_ок and има_главна and има_малка and има_цифра:
    print("МНОГО ДОБРА ПАРОЛА!")
    print("   → Липсва само специален символ")

elif дължина_ок and има_главна and има_цифра:
    print("ДОБРА ПАРОЛА, но може и по-добре")
    print("   → Липсва малка буква и/или специален символ")

elif дължина_ок and има_главна:
    print("СРЕДНА ПАРОЛА")
    print("   → Добави цифри и специални символи")

elif дължина_ок:
    print("СЛАБА ПАРОЛА")
    print("   → Добави главна буква, цифри и специални знаци")

else:
    # тук влизаме само ако паролата е под 8 символа
    print("МНОГО СЛАБА ПАРОЛА!")
    print(f"   → Дължината е само {len(password)} символа (трябват минимум 8)")
    if not има_главна:
        print("   → Липсва главна буква")
    if not има_цифра:
        print("   → Липсват цифри")

print("\nКрай на проверката")