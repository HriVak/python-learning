# –ö–∞–ª–∫—É–ª–∞—Ç–æ—Ä –∑–∞ —Å–º–µ—Ç–∫–∞ –∏ –±–∞–∫—à–∏—à –≤ —Ä–µ—Å—Ç–æ—Ä–∞–Ω—Ç

print ("=== –†–µ—Å—Ç–æ—Ä–∞–Ω—Ç—Å–∫–∏ –∫–∞–ª–∫—É–ª–∞—Ç–æ—Ä ===")

# –í—ä–≤–µ–∂–¥–∞–Ω–µ –Ω–∞ –¥–∞–Ω–Ω–∏
bill = float(input("–ö–æ–ª–∫–æ –µ —Å–º–µ—Ç–∫–∞—Ç–∞ (–≤ –ª–µ–≤–∞)? "))
people = int(input("–ù–∞ –∫–æ–ª–∫–æ –¥—É—à–∏ —Å–µ –¥–µ–ª–∏ —Å–º–µ—Ç–∫–∞—Ç–∞? "))
tip_percent = int(input("–ö–æ–ª–∫–æ –ø—Ä–æ—Ü–µ–Ω—Ç–∞ –±–∞–∫—à–∏—à –∏—Å–∫–∞—Ç–µ –¥–∞ –æ—Å—Ç–∞–≤–∏—Ç–µ (–Ω–∞–ø—Ä. 10, 12, 15)? "))

# –ò–∑—á–∏—Å–ª–µ–Ω–∏—è

tip_amount = bill * (tip_percent / 100)
total = bill + tip_amount
per_person = total / people

# 3. –ö—Ä–∞—Å–∏–≤–æ –ø–æ–∫–∞–∑–≤–∞–Ω–µ —Å 2 –∑–Ω–∞–∫–∞ —Å–ª–µ–¥ –¥–µ—Å–µ—Ç–∏—á–Ω–∞—Ç–∞ –∑–∞–ø–µ—Ç–∞—è
print(f"\n–°–º–µ—Ç–∫–∞—Ç–∞:   {bill:.2f} –ª–≤.")
print(f"–ë–∞–∫—à–∏—à {tip_percent}%:  {tip_amount:.2f} –ª–≤.")
print(f"–û–±—â–æ:    {total:.2f} –ª–≤.")
print(f"–ù–∞ —á–æ–≤–µ–∫:   {per_person:.2f} –ª–≤.")
print("–ü—Ä–∏—è—Ç–Ω–∞ –≤–µ—á–µ—Ä! üçΩÔ∏è")
