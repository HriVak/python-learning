# day-05-guess-the-number-limited.py
import random

print("ПОЗНАЙ ЧИСЛОТО – МАКСИМУМ 7 ОПИТА!")
print("Аз намислих число от 1 до 100. Имаш точно 7 опита!\n")

тайно_число = random.randint(1, 100)
максимум_опити = 7
опити = 0

while опити < максимум_опити:
    опити += 1
    останали = максимум_опити - опити + 1

    # Въвеждане с проверка за валидно число
    while True:
        вход = input(f"Опит {опити}/{максимум_опити} – Твоята догадка (1–100): ")
        try:
            догадка = int(вход)
            break
        except ValueError:
            print("Моля, въведи само цяло число! Опитът не се брои.")

    if догадка < тайно_число:
        print(f"По-голямо! Остават ти {останали-1} опита.")
    elif догадка > тайно_число:
        print(f"По-малко! Остават ти {останали-1} опита.")
    else:
        print(f"БРАВО! Позна за {опити} опита!")
        if опити <= 3:
            print("Ти си абсолютен гений!")
        elif опити <= 5:
            print("Много добре!")
        else:
            print("На косъм!")
        break
else:
    print(f"\nКРАЙ НА ИГРАТА! Изчерпа всичките 7 опита.")
    print(f"Тайното число беше: {тайно_число}")

input("\nНатисни Enter, за да излезеш...")